package EX_DOWHILE;

// WHILE, DO WHILE

// EXERCÍCIO: Escreva um programa para exibir os números de 1 até 50 na tela.

public class A04EX01 {
    public static void main(String[] args){

        int num = 1;

        while(num < 51){
            System.out.println(num);
            num = num + 1;
        }

    }
}
